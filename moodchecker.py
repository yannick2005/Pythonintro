mood = input("What's your mood: ")

if mood == "happy":
    print("Tt is great to see you happy! ")
elif mood == "nervous":
    print("Take a deep breath 3 times. ")
elif mood == "sad":
    print("Can I cheer you up? ")
elif mood == "excited":
    print("Relax. ")
elif mood == "relaxed":
    print("Great! ")
else:
    print("I don't recognize this mood")